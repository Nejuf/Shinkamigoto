          <p>Having a very brief winter, Shinkamigoto is green year-round.  A popular destination are the public <a href='nature.php#beaches'>beaches</a>, noteworthy for exceptionally shallow water and clear sand.  For those who enjoy picnics or leisurely strolls, there are many <a href='nature.php#parks'>parks</a> with great mountain and ocean views.  There are a variety of other nature related <a href='nature.php#misc'>sites</a>, such as the Hifumi Waterfall, that are a bit more hidden but worth the adventure.
            </p>
            <p>&nbsp;
          </p>
